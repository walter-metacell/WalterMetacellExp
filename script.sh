#!/bin/bash

case "$1" in 
  # --------------------------------------------------------------------------------------------------------------
  'init')
    VAR="JUST A VAR"
    SHOW_VAR() {
      echo "$VAR"
    }
    export VAR
    export -f SHOW_VAR
  ;;
  # --------------------------------------------------------------------------------------------------------------
  'run')
    SHOW_VAR
  ;;
  # --------------------------------------------------------------------------------------------------------------
  'run2')
    travis_wait SHOW_VAR
  ;;
esac
